#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int t;

	t = n;
	if (n < 0)
	{
		ft_putchar('-');
		t = -n;
	}
	if (t >= 10)
	{
		ft_putnbr(t / 10);
		ft_putnbr(t % 10);
	}
	if (t < 10)
		ft_putchar(t + 48);
}
