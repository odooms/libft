#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*t;
	size_t	i;

	if ((t = malloc(size)) == NULL)
	{
		return (NULL);
		i = 0;
	}
	while (i < size)
	{
		t[i] = 0;
		i++;
	}
	return (t);
}
