#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t i;

	i = 0;
	if ((dst[i] == '\0' && src[i] != '\0') || (dst[i] != '\0' && src[i] == '\0') || (i > len))
	{
		return (0);
	}
	while (i < len) 
	{
		(dst[i] = src[i]);
		i++;
	}
	return (dst);
}
