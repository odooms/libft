#include <stdio.h>

int    ft_toupper(int a);

int main()
{
    printf("%c\n", ft_toupper('q'));
    return (0);
}

int ft_toupper(int a)
{
	if(a >= 97 && a <= 122)
	{
		a = a - 32;
	}
	return (a);
}
